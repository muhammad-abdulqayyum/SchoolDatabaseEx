/*1-5. Creating DB Tables*/

CREATE TABLE Department
(
department_id number PRIMARY KEY,
department_name varchar(30),
department_block_number number

);

CREATE TABLE Student
(
student_id number PRIMARY KEY,
student_name varchar(30),
address varchar (40),
city varchar(30),
department_id number REFERENCES Department(department_id)

);

CREATE TABLE Staff
(
staff_id number PRIMARY KEY,
staff_name varchar(30),
department_id number REFERENCES Department(department_id)

);

CREATE TABLE Subject
(
subject_id number PRIMARY KEY,
subject_name varchar(30),
subject_code varchar(10),
staff_id number REFERENCES Staff(staff_id)

);

CREATE TABLE Mark
(
value number,
subject_id number REFERENCES Subject(subject_id),
student_id number REFERENCES Student(student_id),

PRIMARY KEY(subject_id, student_id)

);


